#include <stdio.h>

int main(void) {
	int i;

	i = 2;
	
	i = i + 3;	// addition (+) and assignment (=) operators
	i = i - 8;	// subtraction
	i = i / 2;	// division
	i = i * 9;	// multiplication
	i++;		// add one to i (post-increment)
	++i;		// add one to i (pre-increment)
	i--;		// subtract one from i (post-decrement)
	
	return 0;
}
